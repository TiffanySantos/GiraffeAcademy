lucky_numbers = [4, 8, "fifteen", 16, 23, 42.5]

lucky_numbers[0] = 90 # modify item 0
print(lucky_numbers [0]) # print item 0
print(lucky_numbers [1]) # print item 1
print(lucky_numbers [-1]) # print last item in list
print(lucky_numbers [2:]) # print from item 2 to the end
print(lucky_numbers [2:4]) # print from item 2 UP TO BUT NOT INCLUDING item 4
print(len(lucky_numbers)) # print list length

